public class PopcornPopper
{

    public PopcornPopper()
    {
    }

    public void On()
    {
        Console.WriteLine($"PopcornPopper is now ON.");
    }

    public void Pop()
    {
        Console.WriteLine($"PopcornPopper is popping popcorn!");
    }

    public void Off()
    {
        Console.WriteLine($"PopcornPopper is now OFF.");
    }
}